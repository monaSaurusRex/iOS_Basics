/*:
 ## Types and Variables - Naming
 It is best practice to use *camel case* naming convention. This means using a lower case for the first word in the variable name and using upper case for every subsequent word. You can name a variable or constant whatever you want but it is best to use descriptive and intuitive names. The [Swift API Design Guidelines](https://swift.org/documentation/api-design-guidelines/) gives us further guidance on this.
 
 * Include all the words needed to avoid ambiguity
 * Omit needless words.
 * Name variables, parameters, and associated types according to their roles
 
 
 There are limitations on how you name variables or constants. For example, you can not use whitespace, use mathematical symbols or start a variable/constant name with a number.
